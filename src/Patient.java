
public class Patient {
	
	
	private String first;
	private String last;
	private String amka;
	
	
	public Patient(String first,String last,String amka){
		this.first=first;
		this.last=last;
		this.amka=amka;
	}

	public String getFirst() {

		return first;
	}

	public String getLast() {

		return last;
	}

	public String getAMKA() {

		return amka;
	}

}
